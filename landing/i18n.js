let currentLang = localStorage.getItem("lingokey-landing-lang") || "en";

function setLanguage(lang) {
  if (!translations[lang]) return;
  currentLang = lang;
  localStorage.setItem("lingokey-landing-lang", lang);

  document.documentElement.lang = lang === "zh" ? "zh-CN" : "en";

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    const text = translations[lang][key];
    if (text !== undefined) {
      // If element has child SVG or other elements, preserve them and only update text nodes
      if (el.querySelector("svg")) {
        // Find direct text nodes or text-containing spans and update
        const walker = document.createTreeWalker(el, NodeFilter.SHOW_TEXT, null, false);
        let node;
        while ((node = walker.nextNode())) {
          if (node.textContent.trim().length > 0) {
            node.textContent = text;
            break;
          }
        }
      } else {
        el.textContent = text;
      }
    }
  });

  const label = document.getElementById("langLabel");
  if (label) {
    label.textContent = lang === "zh" ? "中" : "EN";
  }
}

function toggleLanguage() {
  setLanguage(currentLang === "en" ? "zh" : "en");
}

// Initialize on DOM ready
document.addEventListener("DOMContentLoaded", () => {
  setLanguage(currentLang);
});
